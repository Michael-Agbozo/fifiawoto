    <h2>About this policy</h2>
    <p>
        This Privacy Policy applies to the websites and online services operated by the Dadaa Fifiawoto Nyamadi Foundation (the "Foundation", "we", "our", "us"). It describes how we handle personal information you share with us when you visit our site, sign up for our newsletter, apply to volunteer, submit a beneficiary application, send us a message, or make a donation.
    </p>
    <p>
        We aim to collect only what we need to run our programmes responsibly, and we treat the information you trust us with as carefully as we treat the people we serve.
    </p>

    <h2>Information we collect</h2>
    <p>We collect information only when you choose to share it with us. That includes:</p>
    <ul>
        <li><strong>Newsletter signups</strong>, your name (optional) and email address.</li>
        <li><strong>Volunteer applications</strong>, your name, email, phone number, country, areas of interest, availability, skills, motivation, and consent timestamp.</li>
        <li><strong>Contact form submissions</strong>, your name, email, phone (optional), subject, and message.</li>
        <li><strong>Beneficiary applications</strong>, your name, contact details, location, the type of assistance you are requesting, a description of your situation, and any supporting documents you choose to upload.</li>
        <li><strong>Donations</strong>, your name, email (optional), the amount you give, and, when applicable, an external payment reference. We do not store full card numbers or bank credentials.</li>
        <li><strong>Account information</strong>, if you have a Foundation account, we store your name, email, role, and (optionally) a phone number and avatar.</li>
        <li><strong>Technical information</strong>, basic data sent by your browser when you visit our site (IP address, browser type, pages requested, time of request). See our <a href="{{ route('legal.cookies') }}" wire:navigate>Cookie Policy</a> for more.</li>
    </ul>

    <h2>How we use your information</h2>
    <ul>
        <li>To deliver the programme, opportunity, or assistance you have applied for.</li>
        <li>To reach out to you about volunteer roles, events, beneficiary cases, and partnership opportunities.</li>
        <li>To send the newsletters and updates you have subscribed to.</li>
        <li>To report on the Foundation's impact (always in aggregate, never identifying you without your consent).</li>
        <li>To operate, protect, and improve our website.</li>
        <li>To meet legal, accounting, and donor-stewardship obligations.</li>
    </ul>

    <h2>Legal basis</h2>
    <p>
        We rely on your <strong>consent</strong> for newsletter signups, volunteer applications, and beneficiary applications. You give that consent by ticking the relevant box before submitting a form. For donations and partnerships we also rely on the <strong>contractual</strong> and <strong>legitimate interest</strong> bases, for example, sending you a tax receipt or confirming a partnership agreement.
    </p>

    <h2>Sharing your information</h2>
    <p>
        We do not sell personal information. We share it only with the people and services that need it to help us operate, and only to the extent necessary:
    </p>
    <ul>
        <li>Foundation staff, board members, and approved volunteers (limited to what their role requires).</li>
        <li>Service providers we use to host the site, send email, process donations, and manage files (each bound by contractual confidentiality and security obligations).</li>
        <li>Authorities, when we are required to by law or to protect someone's safety.</li>
    </ul>

    <h2>International transfers</h2>
    <p>
        The Foundation operates across the United States, Ghana, Togo, and Benin. Your information may be processed in any of these countries by staff or service providers based in those locations. We use appropriate safeguards (including written agreements) when information moves between countries.
    </p>

    <h2>Storage and security</h2>
    <p>
        We host the site and its data with reputable providers and apply technical and organisational measures appropriate to the sensitivity of the information. Beneficiary documents are stored on a private disk and accessed via signed URLs rather than public links. No system is perfectly secure, but we take reasonable care and review our practices regularly.
    </p>

    <h2>How long we keep your information</h2>
    <p>
        We keep information only as long as we need it for the purpose we collected it, to comply with our legal obligations, and to defend our rights if a dispute arises. Newsletter subscribers can unsubscribe at any time; we will retain a record of the unsubscribe so we honour your preference. Beneficiary case files are retained for the length of the support relationship plus a period required by our partners and applicable law.
    </p>

    <h2>Your rights</h2>
    <p>Depending on where you live, you may have the right to:</p>
    <ul>
        <li>Ask what personal information we hold about you.</li>
        <li>Ask us to correct information that is wrong.</li>
        <li>Ask us to delete information when we no longer need it.</li>
        <li>Withdraw consent at any time (where consent is the legal basis).</li>
        <li>Object to certain uses of your information.</li>
        <li>Lodge a complaint with a data-protection authority.</li>
    </ul>
    <p>To make any of these requests, contact us at <a href="mailto:hello@fifiawoto.org">hello@fifiawoto.org</a>.</p>

    <h2>Children</h2>
    <p>
        Some of the people we serve are children, and their information is provided by a parent, guardian, or partner organisation. We do not knowingly collect information directly from children through this website. If you believe a child has submitted information without appropriate adult consent, please contact us so we can address it.
    </p>

    <h2>Cookies</h2>
