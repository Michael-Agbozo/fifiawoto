    <h2>What cookies are</h2>
    <p>
        Cookies are small text files placed on your device by the sites you visit. They are widely used to make sites work, remember your preferences, and provide aggregate information about how the site is being used. Local storage is a related browser feature we use for similar purposes.
    </p>

    <h2>The cookies we use</h2>
    <h3>Strictly necessary</h3>
    <p>
        These are required for the site to function. We use them to keep you signed in once you authenticate, protect form submissions from cross-site request forgery, and preserve the contents of forms while you are filling them in. The site cannot work properly without these.
    </p>

    <h3>Functional</h3>
    <p>
        We may store a small amount of information in your browser's local storage to remember preferences such as your interface appearance (light or dark mode). This data stays on your device.
    </p>

    <h3>Analytics</h3>
    <p>
        If we enable an analytics service to understand how visitors use the site, it will be a privacy-respecting provider configured to anonymise IP addresses and avoid cross-site tracking. We will update this section when that is in place. Today, we use no third-party analytics cookies.
    </p>

    <h2>Third-party services</h2>
    <p>
        Pages that embed third-party content (for example, a YouTube video player or an Instagram feed) may set their own cookies. Those cookies are governed by the third party's policies. You can avoid them by not interacting with the embedded content.
    </p>

    <h2>How to manage cookies</h2>
    <p>
        You can refuse, block, or delete cookies through your browser settings. Doing so for strictly necessary cookies may prevent the site from working, for example, you may not be able to stay signed in or submit forms. Most browsers also let you clear local storage on demand.
    </p>
    <ul>
        <li><strong>Chrome:</strong> Settings → Privacy and security → Cookies and other site data.</li>
        <li><strong>Firefox:</strong> Settings → Privacy &amp; Security → Cookies and Site Data.</li>
        <li><strong>Safari:</strong> Settings → Privacy → Manage Website Data.</li>
        <li><strong>Edge:</strong> Settings → Cookies and site permissions → Manage and delete cookies and site data.</li>
    </ul>

